
CREATE TABLE IF NOT EXISTS users (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    email VARCHAR(255) UNIQUE NOT NULL,
    login VARCHAR(255) UNIQUE NOT NULL,
    birthday DATE NOT NULL
);
CREATE TABLE IF NOT EXISTS genres(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) UNIQUE NOT NULL
);
CREATE TABLE IF NOT EXISTS mpa (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) UNIQUE NOT NULL
);
CREATE TABLE IF NOT EXISTS films (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR (255)  NOT NULL,
    description VARCHAR(255) NOT NULL,
    release_date DATE NOT NULL,
    duration INTEGER NOT NULL,
    mpa_id INTEGER REFERENCES mpa (id) ON DELETE RESTRICT NOT NULL
);
CREATE TABLE IF NOT EXISTS film_genres(
    film_id INTEGER REFERENCES films (id) ON DELETE RESTRICT NOT NULL,
    genre_id INTEGER REFERENCES genres (id) ON DELETE RESTRICT NOT NULL,
    PRIMARY KEY(film_id,genre_id)
);

CREATE TABLE IF NOT EXISTS films_like (
  film_id INTEGER REFERENCES films (id)  ON DELETE RESTRICT NOT NULL,
  user_id INTEGER REFERENCES users (id)  ON DELETE RESTRICT NOT NULL,
  PRIMARY KEY (film_id,user_id)
);

CREATE TABLE IF NOT EXISTS friends (
    user_id INTEGER REFERENCES users (id)  ON DELETE RESTRICT NOT NULL,
    friend_id INTEGER REFERENCES users (id) ON DELETE RESTRICT  NOT NULL,
    PRIMARY KEY (user_id,friend_id)
);